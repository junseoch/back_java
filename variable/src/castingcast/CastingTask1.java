package castingcast;

public class CastingTask1 {
	public static void main(String[] args) {
		// 정수 + 실수 = 실수
		
//		8.43 + 2.59를 더한 값이 10이 되도록 출력(형변환)
		
		double num1 = 8.43, num2 = 2.59;  
		int result = 0;
		result = (int)num1 + (int)(num2);
		
		System.out.printf("%d",result);
		// 8 + 2
		
		System.out.println("");
		
		// 아스키코드
		System.out.println('A'+ 10);
		
		// 자동 형변환 char + int = int
  		// 문자형 + 정수 = 정수
		
		
		
	}
}
